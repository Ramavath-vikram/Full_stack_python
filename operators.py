#logical operator(and,or,not)
#logical operator is used to perform logical operation between operands
print(16>8 and 4<6)
print(16>8 or 4<6)
print(not(16>8 and 4<6))
#comparison operator(==,!=,>,<,>=,<=)
print(5==5)
print(5!=5)
print(5>5)
print(5<5)
print(5>=5)
print(5<=5)
